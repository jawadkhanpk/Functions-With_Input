
# Parameters = name of the data
# Arguments  = The actual data

def greet_with(name, location):     # here name and location both are parameters of a function
    print(f"Hello {name}")
    print(f"What is it like in {location}")

greet_with("jawad","abbottabad")  # here jawad and abbottabad are the arguments of a function
# in line 9 jawad and abbottabad are also positional argument